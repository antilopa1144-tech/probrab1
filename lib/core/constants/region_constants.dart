/// Константы регионов и ценовые коэффициенты.
class RegionConstants {
  /// Поддерживаемые регионы РФ
  static const List<String> regions = [
    'Москва',
    'Санкт-Петербург',
    'Екатеринбург',
    'Краснодар',
    'Новосибирск',
    'Казань',
    'Нижний Новгород',
    'Челябинск',
    'Самара',
    'Регионы РФ',
  ];

  /// Ценовые коэффициенты по регионам (относительно Москвы = 1.0)
  static const Map<String, double> priceCoefficients = {
    'Москва': 1.0,
    'Санкт-Петербург': 0.95,
    'Екатеринбург': 0.75,
    'Краснодар': 0.80,
    'Новосибирск': 0.70,
    'Казань': 0.72,
    'Нижний Новгород': 0.73,
    'Челябинск': 0.68,
    'Самара': 0.71,
    'Регионы РФ': 0.65,
  };

  /// Коэффициенты стоимости работ по регионам
  static const Map<String, double> laborCoefficients = {
    'Москва': 1.0,
    'Санкт-Петербург': 0.90,
    'Екатеринбург': 0.60,
    'Краснодар': 0.65,
    'Новосибирск': 0.55,
    'Казань': 0.58,
    'Нижний Новгород': 0.60,
    'Челябинск': 0.52,
    'Самара': 0.57,
    'Регионы РФ': 0.50,
  };

  /// Получить ценовой коэффициент региона
  static double getPriceCoefficient(String region) {
    return priceCoefficients[region] ?? 0.65;
  }

  /// Получить коэффициент стоимости работ
  static double getLaborCoefficient(String region) {
    return laborCoefficients[region] ?? 0.50;
  }
}
